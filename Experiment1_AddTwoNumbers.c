// Experiment1_AddTwoNumbers.c: Add two numbers entered by the user
#include <stdio.h>
int main() {
    int a, b;
    printf("Enter first number: ");
    scanf("%d", &a);
    printf("Enter second number: ");
    scanf("%d", &b);
    printf("The sum is %d\n", a+b);
    return 0;
}
